//While loops
var b = 10; //sets up index

while (b > 0){ //checks condition
    console.log(b + " kegs on the wall." );
    b--;//increments (++) or decrements (--) the index
}



var c = 10;

// Do/while loops
do{
    console.log(c + " kegs on the wall.");
    c--;
}
while (c > 0);



//For loops

for (var i = 10; i > 0; i--){
    console.log(i + " kegs on the wall.");
}

